let ok = 0;
let fail = 0;

for (const [locale, keys] of Object.entries(translations)) {
  const filePath = path.join(LOCALES_DIR, locale, 'common.json');
  try {
    const raw = fs.readFileSync(filePath, 'utf8');
    const data = JSON.parse(raw);

    let changed = false;
    for (const [section, values] of Object.entries(keys)) {
      if (!data[section]) {
        data[section] = values;
        changed = true;
      } else {
        for (const [key, val] of Object.entries(values)) {
          if (data[section][key] === undefined) {
            data[section][key] = val;
            changed = true;
          }
        }
      }
    }

    if (changed) {
      fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
      console.log(`[OK] ${locale}`);
      ok++;
    } else {
      console.log(`[SKIP] ${locale} — already up to date`);
      ok++;
    }
  } catch (e) {
    console.error(`[FAIL] ${locale}: ${e.message}`);
    fail++;
  }
}

console.log(`\nDone: ${ok} ok, ${fail} failed`);
